
package fortest;

public class forEx {

	public static void main(String[] args) {
	//1~10까지의 합을 구하면
	//+= 이거사용
		int sum=0;
		for(int a=1; a<=10; a++)
		{ 
		    sum += a;
		}
		System.out.println("1부터10까지의 합은"+sum);
		
		for(int a=1; a<=5; a++)
		{
			System.out.print("*");
		}
}
}
